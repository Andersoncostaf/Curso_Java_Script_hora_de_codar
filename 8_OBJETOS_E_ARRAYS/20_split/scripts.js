// Definindo uma string
let frase = "Testando o método split Anderson Fox";

// Utilizando o método split para dividir a string em um array de palavras, usando o espaço como delimitador
console.log(frase.split(" "));

// Atribuindo o resultado a uma variável e imprimindo o array de palavras
let palavras = frase.split(" ");
console.log(palavras);

// Definindo uma string contendo produtos separados por ponto e vírgula
let produtos = "Banana;Maça;Jaca;Pera;Bola;Tapete";

// Utilizando o método split para dividir a string em um array de produtos, usando o ponto e vírgula como delimitador
console.log(produtos.split(';'));




