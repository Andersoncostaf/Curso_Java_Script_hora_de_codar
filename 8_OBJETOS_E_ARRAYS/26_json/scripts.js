// Criando um objeto chamado pessoa com propriedades nome e idade
let pessoa = {
  "nome": "Anderson",
  "idade": 35
}

// Imprimindo o valor da propriedade "nome" do objeto pessoa
console.log(pessoa.nome); // Saída: Anderson

// Imprimindo o valor da propriedade "idade" do objeto pessoa
console.log(pessoa.idade); // Saída: 35
