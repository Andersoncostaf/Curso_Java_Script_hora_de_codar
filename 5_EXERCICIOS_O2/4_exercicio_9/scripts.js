/*
A função Math.pow() exibe a potencia de um número;

Teste a função com console.log() e as seguintes bases: 
2,3, 18 e o expoente deve ser 2;

Recebe 2 argumentos, base e expoente;

Ex: Math.pow(53);
*/

const num = 18;

console.log(Math.pow(2, 2));    // 2 elevado à 2ª potência (2^2) = 4
console.log(Math.pow(3, 2));    // 3 elevado à 2ª potência (3^2) = 9
console.log(Math.pow(num, 2));  // O valor de 'num' (18) elevado à 2ª potência (18^2) = 324
